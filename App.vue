                }
                lat = parseFloat(lat);
                lng = parseFloat(lng);

                // #ifdef MP-WEIXIN
                // 微信小程序使用【腾讯位置服务路线规划】插件
                if (this.data.is_weixin_open_location_use_plugins == 1) {
